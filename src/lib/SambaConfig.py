@dataclass
class WarningEntry:
    title: str
    description: str

@dataclass
class SambaShare:
    name: str
    share_path: str
    comment: str
    writeable: bool
    public: bool
    valid_users: list = field(default_factory=list)
    force_user: str | None = None

    @staticmethod
    def create_empty():
        return SambaShare(
            name='',
            share_path='',
            comment='',
            writeable=True,
            public=True,
            valid_users=[],
            force_user=None
        )
    
@dataclass
class SambaUser:
    user: str
    uid: str
    comment: str

class SambaConfig():
    NM_TOGGLE_SCRIPT = '/etc/NetworkManager/dispatcher.d/90-samba-toggle.sh'
    DEFAULT_SECTION = 'global'
    PRINTERS_SECTION = 'printers'
    DEFAULT_GLOBAL_SECTION = {
        'workgroup': 'WORKGROUP',
        'netbios name': '',
        'server string': '%h server (Samba)',
        'server role': 'standalone server',
        'log file': '/var/log/samba/log.%J',
        'max log size': '1000',
        'logging': 'file',
        'map to guest': 'bad user',
        # Keep samba and unix users separated, makes everything easier
        'unix password sync': 'no',
        'pam password change': 'no',
        # Disable usershare to keep everything under control
        'usershare max shares': '0',
        'passwd chat': '"new password" %n\\n "retype new password" %n\\n "Password changed"'
    }
    REQUIRED_CLI_TOOLS = ['testparm', 'smbd', 'smbcontrol', 'smbstatus']
    RESERVED_SECTIONS = ['homes', 'printers', 'global', 'print$']
    COMMENTS_LINE_START = ['#', ';']
    REQUIRED_HEADER_LINE = '# - This configuration file was automatically generated by Simba -'
    CONFIG_FILE_HEADER = """
        #
        # - This configuration file was automatically generated by Simba -
        # A GUI tool to manage the Samba server.
        #
        # For addiitonal information about smb.com, please visit the following website:
        # https://www.samba.org/samba/docs/current/man-html/smb.conf.5.html
        #
        # For addiitonal information about Simba, please visit the following website:
        # https://github.com/mijorus/simba
        #
        #
        # If you still want to manager your server using Simba, 
        # do not remove the following lines.
        #
        # Hash: $file_hash
        #
    """

    def __init__(self, override_config_file_location=None) -> None:
        self.config_file_location = utils.mapped_path('/etc/samba/smb.conf')
        self.sys_config_file_location = self.config_file_location
        self.tmp_config_file_location = GLib.get_user_cache_dir()
        self.service_name = 'smb.service'

        if override_config_file_location:
            self.config_file_location = override_config_file_location

        self._config_file_content = ''
        self.data = configparser.ConfigParser(
            comment_prefixes=self.COMMENTS_LINE_START,
            inline_comment_prefixes=self.COMMENTS_LINE_START,
            strict=True,
            interpolation=None,
            default_section=self.DEFAULT_SECTION
        )

        for s in ['smb.service', 'smbd.service']:
            try:
                terminal.host_sh(['systemctl', 'list-unit-files', s])
                self.service_name = s
                break

            except Exception as e:
                pass

        if os.path.exists(self.config_file_location):
            self.reload()

    @staticmethod
    def get_samba_version():
        smbv = terminal.host_sh(['smbd', '--version'])
        return smbv.replace('Version', '').strip().split('.')

    @staticmethod
    def test_compatibility():
        for tool in SambaConfig.REQUIRED_CLI_TOOLS:
            try:
                terminal.host_sh(['command', '-v', tool])
            except Exception as e:
                logging.warning('Missing required CLI tool: ' + tool)
                return False
        
        if SambaConfig.get_samba_version()[0] != '4':
            return False
        
        smbservice = False
        for s in ['smb.service', 'smbd.service']:
            try:
                terminal.host_sh(['systemctl', 'list-unit-files', s])
                smbservice = True
            except Exception as e:
                pass
        
        if not smbservice:
            return False

        return True

    @staticmethod
    def scan_warnings():
        _warnings: list[WarningEntry] = []

        try:
            terminal.host_sh(['which', 'getsebool'])
            resp = terminal.host_sh(['getsebool', 'samba_enable_home_dirs'])
            if resp.endswith('> off'):
                _warnings.append(WarningEntry(
                    title=_('SELinux'),
                    description=_('SELinux policy "samba_enable_home_dirs" is off, this might cause issues when trying to share folder in your home directory')
                ))
        except Exception as e:
            pass

        try:
            terminal.host_sh(['which', 'firewall-cmd'])
            resp = terminal.host_sh(['firewall-cmd', '--list-services'])
            if 'samba' not in resp.split(' '):
                _warnings.append(WarningEntry(
                    title=_('Firewall'),
                    description=_('A firewall rule might be blocking SAMBA on this computer')
                ))
        except Exception as e:
            pass

        return _warnings

    def is_config_supported(self):
        if self.REQUIRED_HEADER_LINE not in self._config_file_content:
            logging.warning('required header line not found')
            return False

        pattern = r'# Hash:\s*([a-fA-F0-9]{32})'
        match = re.search(pattern, self._config_file_content, re.MULTILINE)

        if not match:
            logging.warning('hash not found')
            return False

        stored_hash = match.group(1)
        current_hash = self.get_md5(file_path=self.config_file_location)

        if stored_hash != current_hash:
            logging.warning('hash do not match:')

        return stored_hash == current_hash

    def init_with_defaults(self):
        self.data.clear(    )
        self.data['global'] = self.DEFAULT_GLOBAL_SECTION

    def save(self, allowed_networks=None):
        text_content = self._get_text_content()

        random_string = utils.get_random_md5()
        testfile_path = os.path.join(self.tmp_config_file_location, f'{random_string}.conf')
        config_file_hash = self.get_md5(content=text_content)
        text_header = dedent(self.CONFIG_FILE_HEADER).replace('$file_hash', config_file_hash)
        text_content = '\n'.join([text_header, text_content])

        with open(testfile_path, 'w+') as f:
            f.write(text_content)

        # check file validity
        terminal.host_sh(['testparm', '--suppress-prompt', testfile_path])
        nm_dispactech_script = None
        nm_dispactech_script_path = ''

        if allowed_networks != None:
            nm_dispactech_script = self.create_nm_dispatcher_script(target_networks=allowed_networks)
            nm_dispactech_script_path = nm_dispactech_script.path

        save_script = ShellScript(
            filename='save_samba_config.sh',
            content="""
                set -e

                ENABLE_NM_D="$enable_nm_d"

                cp $location $location_old
                cp $testfile_path $location

                if command -v nmcli &> /dev/null; then
                    if [[ $$ENABLE_NM_D == "1" ]]; then
                        if [ -d "/etc/NetworkManager/dispatcher.d" ]; then
                            mv $nm_dispactech_script_path $nm_dispactech_script_dest

                            chmod 744 $nm_dispactech_script_dest
                            chown root:root $nm_dispactech_script_dest

                            # Execute this bash script like there was a network change
                            bash $nm_dispactech_script_dest 0 up
                            exit 0
                        fi
                    else
                        rm -f $nm_dispactech_script_dest
                    fi
                fi

                systemctl restart $service
            """,
            testfile_path=testfile_path,
            service=self.service_name,
            location=self.sys_config_file_location,
            enable_nm_d=('1' if allowed_networks != None else '0'),
            nm_dispactech_script_path=nm_dispactech_script_path,
            nm_dispactech_script_dest=self.NM_TOGGLE_SCRIPT,
            location_old=f'{self.sys_config_file_location}.simba.old'
        )

        save_script.root_host_execute()

        if os.path.exists(testfile_path):
            os.remove(testfile_path)

        save_script.delete()
        self.reload()

    def get_md5(self, file_path=None, content=''):
        if (not content) and (not file_path):
            raise Exception('Missing content and file_path')

        if file_path:
            with open(file_path, 'r') as f:
                content = f.read()

        valid_content = ''
        for line in content.split('\n'):
            skip_line = False
            line = line.strip()
            for s in self.COMMENTS_LINE_START:
                skip_line = (not line) or line.startswith(s)
                
                if skip_line:
                    break
            
            if not skip_line:
                valid_content += (line + '\n')

        filehash = hashlib.md5(valid_content.encode()).hexdigest()

        return filehash

    def get_section(self, section: str):
        return self.data[section]
    
    def create_section(self, section: str, data: dict):
        self.data[section] = data

    def check_valid_share_name(self, name: str) -> tuple:
        if name in self.RESERVED_SECTIONS:
            return (False, 'name reserved')
        
        if len(name) > 8:
            return (False, 'name is too long')
        
        return (True, '')

    def delete_share(self, share: SambaShare):
        if share.name not in self.data.keys():
            raise Exception(f'{share.name} does not exists! Available shares are: {self.data.keys()}')
        
        del self.data[share.name]

    def create_share(self, share: SambaShare):
        name_check, name_check_error = self.check_valid_share_name(share.name)
        if not name_check:
            raise Exception(f'{share.name}: ' + name_check_error)
        
        section_content = {
            'path': share.share_path,
            'writeable': self.bool_value_to_str(share.writeable),
            'public': self.bool_value_to_str(share.public),
            'comment': share.comment,

            # MacOS stuff
            'veto files': '/.DS_Store/._.*/',
            'delete veto files': self.bool_value_to_str(True)
        }

        if share.force_user:
            section_content['force user'] = share.force_user

        self.create_section(share.name, section_content)

    def is_printing_supported(self):
        try:
            terminal.host_sh(['systemctl', 'status', 'cups.service'])
        except Exception as e:
            return False
        
        return True

    def bool_value_to_str(self, val: bool):
        return 'yes' if val else 'no'

    def list_shares(self) -> list[SambaShare]:
        shares = []
        for section, data in self.data.items():
            if f'{section}' in self.RESERVED_SECTIONS:
                continue

            writeable = data.getboolean('writeable', True)

            share = SambaShare(
                name=section,
                share_path=data.get('path', ''),
                comment=data.get('comment', ''),
                writeable=writeable,
                public=data.getboolean('public', True),
                force_user=data.get('force user', None)
            )

            shares.append(share)

        return shares

    def create_nm_dispatcher_script(self, target_networks):
        nm_script = utils.get_asset('/it/mijorus/simba/assets/90-samba-toggle.sh')
        bash_location = terminal.host_sh(['which', 'bash'])
        target_networks_names = ', '.join([f'"{t.name}"' for t in target_networks])
        target_networks_val = ' '.join([f'"{t.uuid}"' for t in target_networks])

        nm_script = nm_script.replace('#!/bin/bash', f'#!{bash_location}', 1)
        nm_script = nm_script.replace('# Network names:', f'# Network names: {target_networks_names}', 1)
        nm_script = nm_script.replace('TARGET_NETWORKS=()', f'TARGET_NETWORKS=({target_networks_val})', 1)
        nm_script = nm_script.replace('SAMBA_SERVICE=""', f'SAMBA_SERVICE="{self.service_name}"', 1)

        scr = ShellScript(filename='nm_dispatcher', content=nm_script)
        return scr

    def remove_nm_script(self):
        pass

    def get_nm_allowed_networks(self):
        try:
            trg = terminal.host_sh(['grep', '-oP', "TARGET_NETWORKS=\\(\\K[^)]*", self.NM_TOGGLE_SCRIPT])
        except Exception as e:
            return []
        
        uuids = [f.replace('"', '') for f in trg.split(' ')]
        return uuids
    
    def has_toggle_script(self):
        try:
            terminal.host_sh(["test", '-f', self.NM_TOGGLE_SCRIPT])
        except Exception as e:
            return False
        
        return True

    def set_print_service(self, enabled, restricted_users: Optional[list[SambaUser]]=None):
        if enabled:
            self.data[self.DEFAULT_SECTION]['load printers'] = self.bool_value_to_str(True)
            self.data[self.DEFAULT_SECTION]['printing'] = 'cups'  # only CUPS service is supported for now
            self.data[self.DEFAULT_SECTION]['printcap name'] = 'cups'

            config = {
                'comment': 'All printers',
                'printable': self.bool_value_to_str(True),
                'path': '/var/spool/samba',
            }

            if restricted_users is None:
                config['guest ok'] = self.bool_value_to_str(True)
            else:
                config['valid users'] = ', '.join([el.user for el in restricted_users])

            self.create_section(self.PRINTERS_SECTION, config)
        else:
            for k in ['printing', 'printcap name']:
                if k in self.data[self.DEFAULT_SECTION]:
                    del self.data[self.DEFAULT_SECTION][k]

            self.data[self.DEFAULT_SECTION]['load printers'] = self.bool_value_to_str(False)

            if self.data[self.PRINTERS_SECTION]:
                del self.data[self.PRINTERS_SECTION]

    def is_service_active(self):
        try:
            terminal.host_sh(['systemctl', 'is-active', self.service_name, '--quiet'])
        except Exception as e:
            return False
        
        return True

    @staticmethod
    def create_user(user, passwd):
        pass_str = f'{passwd}\\n{passwd}\\n'
        ShellScript(
            filename='create_samba_user.sh',
            content="""
                set -e
                echo -ne $passwd | pdbedit --create --password-from-stdin $user
            """,
            passwd=pass_str,
            user=user
        ).root_host_execute()

    @staticmethod
    def list_users() -> list[SambaUser]:
        data = terminal.host_sh(['pkexec', 'pdbedit', '--list'], hide_log=True)
        data = data.strip()
        output = []

        for line in data.split('\n'):
            spl = line.split(':')

            if len(spl) < 3:
                continue

            u, uid, comment = line.split(':')
            user = SambaUser(user=u, uid=uid, comment=comment)
            output.append(user)

        return output

    def _get_text_content(self):
        # 1. Create the string buffer
        string_stream = io.StringIO()

        # 2. Write the config object into the buffer
        self.data.write(string_stream)

        # 3. Retrieve the string value
        config_string = string_stream.getvalue()
        return config_string

    def reload(self):
        """Parse a smb file"""
        with open(self.config_file_location, 'r') as f:
            self._config_file_content = f.read()

        self.data.clear()
        self.data.read(self.config_file_location)        
